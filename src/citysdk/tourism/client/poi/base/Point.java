package citysdk.tourism.client.poi.base;


/**
 * Representation of the Point class of the UML Diagram found in:
 * <a href="http://www.w3.org/2010/POI/documents/Core/core-20111216.html#poi-data-model">http://www.w3.org/2010/POI/documents/Core/core-20111216.html#poi-data-model.</a>
 * 
 * <p>A {@link Point} is merely a single coordinate.</p>
 * 
 * @author Pedro Cruz
 */
public class Point extends POITermType {
	private Geometry point;
	
	public Point() {
		super();
		point = null;
	}

	public Geometry getPoint() {
		return point;
	}

	public void setPoint(Geometry point) {
		this.point = point;
	}
}
